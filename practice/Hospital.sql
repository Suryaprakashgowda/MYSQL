-- Hospital database schema

## //DOCTOR

CREATE TABLE DOCTOR(

DOC_ID INT NOT NULL PRIMARY KEY,

D0C_NAME VARCHAR(50),

SPECAILIZATION VARCHAR(50));



## //PATIENTS;

CREATE TABLE PATIENTS(

PAT_ID INT NOT NULL PRIMARY KEY,

PAT_NAME VARCHAR(50),

DOD DATE,

GENDER CHAR(1));



## //APPOINTMENTS

CREATE TABLE APPOINTMENTS(

APP_ID INT NOT NULL PRIMARY KEY,

DOC_ID INT,

PAT_ID INT,

APP_DATE DATETIME,

STATUS ENUM('SCHEDULED','COMPLETED','CANCELLED'),

CONSTRAINT FOREIGN KEY(DOC_ID) REFERENCES DOCTOR(DOC_ID),

CONSTRAINT FOREIGN KEY(PAT_ID) REFERENCES PATIENTS(PAT_ID));





## //MEDICATION

CREATE TABLE MEDICATION(

MED_ID INT NOT NULL PRIMARY KEY,

PAT_ID INT,

DOC_ID INT,

MEDICINE_NAME VARCHAR(100),

DOSAGE VARCHAR(50),

START_DATE DATE,

END_DATE DATE,

CONSTRAINT FOREIGN KEY(PAT_ID) REFERENCES PATIENTS(PAT_ID),

CONSTRAINT FOREIGN KEY(DOC_ID) REFERENCES DOCTOR(DOC_ID));



# //BILLS

CREATE TABLE BILLS(

BILL_ID INT NOT NULL PRIMARY KEY,

APP_ID INT,

AMOUNT DECIMAL(10,2),

PAYMENT_STATUS ENUM('PAID','UNPAID'),

CONSTRAINT FOREIGN KEY(APP_ID) REFERENCES APPOINTMENTS(APP_ID));

