-- Tour Database



## //CUSTOMER

CREATE TABLE CUSTOMER(

CUSTOMER_ID INT NOT NULL PRIMARY KEY,

FIRST_NAME VARCHAR(20) NULL,

LAST_NAME VARCHAR(30),

EMAIL VARCHAR(40),

PHONE_NUMBER VARCHAR(40));



## //DESTINATION

CREATE TABLE DISTINATION(

DESTINATION_ID INT NOT NULL PRIMARY KEY,

DESTINATION_NAME VARCHAR(100),

STATE VARCHAR(40),

AVERGAE_COST DECIMAL(10,2));





## //PACKAGE

CREATE TABLE PACKAGES(

PACKAGE_ID INT NOT NULL PRIMARY KEY,

DESTINATION_ID INT,

PACKAGE_NAME VARCHAR(100),

DURATION_DAYS INT,

PRICE DECIMAL(10,2),

CONSTRAINT FOREIGN KEY(DESTINATION_ID) REFERENCES DISTINATION(DESTINATION_ID));



## //BOOKING



CREATE TABLE BOOKING(

BOOKING_ID INT NOT NULL PRIMARY KEY,

CUSTOMER_ID INT NULL,

PACKAGE_ID INT NULL,

BOOKING_DATE DATE NULL,

STATUS ENUM('CONFIRMED','CANCELLED','PENDING'),

CONSTRAINT FOREIGN KEY(CUSTOMER_ID) REFERENCES CUSTOMER(CUSTOMER_ID),

CONSTRAINT FOREIGN KEY(PACKAGE_ID) REFERENCES PACKAGES(PACKAGE_ID)

);
